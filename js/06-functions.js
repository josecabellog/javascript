

// funciones en JavaScript 

// declaracion de una funcion 
function greet() {
    console.log("Hello!")
}

// invocacion de una funcion
console.log(greet())

// funcion flecha 
const greetArrow = () => {
    console.log("Hello!")
}
console.log(greetArrow())

//funcion expresion 
const greetExpression = function() {
    console.log("Hello!")
}
console.log(greetExpression())
